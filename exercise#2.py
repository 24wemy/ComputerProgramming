# mengkonversi suhu dari celcius ke fahrenheit

# rumus : (celcius x 9 / 5) + 32 
a = int(input("celcius = ")) # 50 derajat celcius
b = int(input("rumus 1 = ")) # 9
c = int(input("rumus 2 = ")) # 5
d = int(input("rumus 3 = ")) # 32

fahrenheit = a * b // c + d  # = 122
print(fahrenheit)